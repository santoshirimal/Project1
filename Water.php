<h3>Rafting</h3>
<p id="water">
<p>Rafting  is one of the best ways to explore the typical cross section of natural as well as ethno-cultural heritage of the country.
 There are numerous rivers in Nepal which offer excellent rafting or canoeing experience. You can glide on calm jade waters with 
 munificent scenery all about or rush through roaring white rapids, in the care of expert river-men employed by government authorized 
 agencies. One can opt for day of river running or more. So far, the government has opened sections of 10 rivers for commercial rafting.
 The Trisuliriver  is one of the most popular of Nepal’s raftable rivers. The Kali Gandaki winds through remote canyons and deep gorges 
 for five days of intense rapids. The BhoteKoshi is 26km of continuous white water and the raging Marshyanghi is four days of uninterrupted white water. 
 The Karnaliriver provides some of the most challenging rapids in the world. The Sun Koshi , 27km, requiring 8-10 days to complete, is a big and challenging river.
 Adventurers are provided with world-class services by rafting agents. Agencies here provide life jackets, camping and the standard rafting paraphernalia needed by
 world-class rafting. An extremely popular sport in Europe, 
cannoning is now available in Nepal. Cannoning gives you the freedom to explore some of the most ruggedly beautiful, yet forbidden places in the world.</p>
