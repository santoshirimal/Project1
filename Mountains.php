<P> When you think of mountains in Nepal, Mount Everest is the most famous attraction of Nepal which is the world's highest peak.
 But Nepal is home to many other mountains, from the 3,000-foot summits of the Churiya Hills to the giants of the Greater Himalayas 
 and the ranges along the border with Tibet.
 .</p>
 <h3>Highest Mountains</h3>
 <p>Highest Mountains
Nepal's most famous mountain is also the highest in the world. Mount Everest stands 29,029 feet above sea level.
 The border between Nepal and China runs directly across Everest's summit. You can climb Everest with the help
 of a professional guide; although the climbing is not the most technically difficult in Nepal, altitude sickness 
 and weather pose serious hazards for Everest climbers. Nepal's second-highest peak is Kangchenjunga, straddling 
 the border with India at 28,169 feet above sea level. The name Kangchenjunga -- which applies to the peak and 
 the surrounding Himalayan area -- translates as "the five treasures of snows." Until the middle of the 19th
 century, Kangchenjunga was believed to be the world's highest mountain. Local people believe the summit 
is sacred, so most climbing parties stop a little distance short of the summit in respect for these beliefs.</p>

<h3>Most Popular Mountains Higher Than 26,000 Feet</h3>
<p>
More than half the foreign adventure tourists entering Nepal travel to the Annapurna massif. This section of the Himalayas 
includes 14 peaks higher than 22,970 feet, including the 26,545-foot summit Annapurna I. The Kali Gandaki Gorge immediately
 to the west of Annapurna is the deepest gorge in the world. The Annapurna peaks are dangerous to climb. After Annapurna,
 the Khumbu region receives most international visitors. This mountain area includes Mount Everest and other peaks. Lhotse
 , a mountain adjoining Everest, has a summit at 27,940 feet. This makes Lhotse the world's fourth-highest mountain, 
 and at the time of publication it was one of the most popular high peaks in Nepal. Climbers ascending Everest by the
 most common route spend part of their climb on Lhotse's northwest face.</p>

<h3>Most Popular Mountains Lower Than 26,000 Feet</h3>
<p>Although less lofty than Everest and Nepal's highest peaks, several mountains under the 26,000-foot mark are popular
 with international travelers. Imja Tse, at 20,210 feet, is cited by Colorful Nepal as one of the country's most popular 
 trekking peaks. Kala Pattar, at 18,192 feet, is located below the higher Pumori mountain and is popular with hikers.</p>

<h3>Unclimbed Mountains</h3>
<p>At the time of publication, some of Nepal's mountains had never been climbed. Mount Khumbila, in northeast Nepal,
 stands 18,901 feet above sea level.Local people consider the mountain too sacred to climb. In the second half of the
 20th century one climbing group made an attempt to climb Mount Khumbila, but perished in an avalanche. The mountain 
 Dragmarpo Ri also remained unclimbed at the time of publication. Sitting on the border between Nepal and China, 
 Dragmarpo Ri is 21,581 feet high</p>
