<h2>Religion:</h2>
<p>Nepal was declared a secular country by the Parliament on May 18, 2006. Religions practiced in Nepal are: Hinduism,
 Buddhism, Islam, Christianity, Jainism, Sikhism, Bon, ancestor worship and animism. The majority of Nepalis are either
 Hindus or Buddhism. The two have co-existed in harmony through centuries.Nepalis do not eat beef. There are several reasons 
 for this, one being that the Hindus worship cow. Cow is also the national animal of Nepal.
 Nepali follow the cast system . Lower cast are not allowed to marry higher cast . If that happens lower cast 
 people have to leave the place they are living . Howeever cast system is changing little bit, some girls/boys are allowed to marry 
 different cast girls/boys with parents acceptance.</p><br/>

