<P> Nepal does not have a distinct cooking style.
 However, food habits differ depending on the region. 
 Nepali food has been influenced by Indian and Tibetan styles of cooking.
 Authentic Nepali taste is found in Newari and Thakai cuisines.
 Most Nepalis do not use cutlery but eat with their right hand.
 The regular Nepali meal is dal (lentil soup), bhat (boiled rice) and tarkari (curried vegetables), 
 often accompanied by achar (pickle).
 Curried meat is very popular. Momos (steamed or fried dumplings) is one of the most popular snack among Nepalis.
 Rotis (flat bread) and dhedo (boiled flour) also make meals in some homes.</p>
 
